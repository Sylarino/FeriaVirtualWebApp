            <h3>La fruta ha de comerse sola</h3>
            <p>La fruta es el alimento que más rápido se digiere por su alto contenido en agua. Si la consumimos combinada con otro alimento (sobre todo a los postres), permanecerá demasiado rato en el estómago ya que los otros alimentos necesitan mayor tiempo de digestión. Todo ello hará que fermente y se pudra. Procura comer la fruta con un espacio de al menos una hora con respecto a la ingesta 
            de cualquier otro alimento y recuerda que la fruta con la leche es una de las peores mezclas que puedes hacer.</p>
            <h3>Sin quitar la piel</h3>
            <p>Si vamos a consumir fruta corriente del supermercado mejor pelarla, aún a riesgo de perder nutrientes, porque la mayoría de productos químicos con lo que ha sido tratada se quedarán en la piel  (y parte penetrará al interior). Pero si consumimos fruta ecológica es mejor no quitar la piel,
            ya que esta lleva la mayor parte de vitaminas y minerales.</p>
            <h3>A fuego lento</h3>
            <p>Si vamos a comer la fruta cocida, entonces trata de mantenerla muy poco tiempo y con fuego muy lento, ya que las vitaminas resisten mal el calor (este principio se aplica a cualquier 
            vegetal).</p>
            <h3>Frutas ácidas y frutas dulces</h3>
            <p>Es mejor evitar el consumo excesivo de frutas ácidas, sobre todo por la tarde y la noche. La mejor hora para comer una naranja o un kiwi, por ejemplo, es por la mañana, y sin acompañar de ningún otro alimento. Si vas a comer más de una fruta, procura que tengan el mismo grado de acidez, es decir, o todas ácidas o todas dulces, y no las mezcles.</p>
            <h3>Fruta recién cortada</h3>
            <p>Para disfrutar de todas las propiedades de una pieza de fruta, lo mejor es comerla madura, recién cortada, entera y nunca al cabo de un rato de haberla troceado, pues la oxidación habrá hecho que se pierdan la mayor parte de sus propiedades.</p>
